    private void comprobarLetraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comprobarLetraActionPerformed
        // TODO add your handling code here:       
        String palabraIntento = textoLetra.getText();        
        ch = palabraIntento.charAt(0);
        textoLetra.setText("");
        if (palabraIntento.length()>1){
            if(!palabraIntento.equals(palabra)){
            intentos.setText(intentos.getText()+ "Esa no es la palabra\n");
            contador++;      
            } else {
                statusText.setText("La palabra era "+palabra+"\nEl numero de fallos acumulados es de "+contador+"\nEl tiempo empleado es de "+(System.currentTimeMillis()-tiempoInicial)/1000+" segundos");
                dialog.setVisible(true);
            }           
        } else {            
            if (!objeto.yaEstaEnPalabra(palabraOculta, ch)){
            palabraOculta = objeto.obtenerIntento(palabra, palabraOculta,ch);        
                if (!objeto.esCorrecta){
                    intentos.setText(intentos.getText()+"Lo siento, "+ch+" no está en la palabra.\n");
                    contador++;                    
                    statusText.setText(statusText.getText()+ch+" ");
                } else {                    
                    intentos.setText(intentos.getText()+"Bien! ");            
                }
            }        
            else {
                intentos.setText(intentos.getText()+ch+ " está ya en la palabra.\n");
            }           
            intentos.setText(intentos.getText()+palabraOculta+"\n");        
        }
        if(palabraOculta.equals(palabra)){            
            statusText.setText("La palabra era "+palabra+"\nEl numero de fallos acumulados es de "+contador+"\nEl tiempo empleado es de "+(System.currentTimeMillis()-tiempoInicial)/1000+" segundos");
            dialog.setVisible(true);
        }                
    }//GEN-LAST:event_comprobarLetraActionPerformed

