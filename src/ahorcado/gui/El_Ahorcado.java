    public static void jugar(){        
        textoLetra.setText("");
        intentos.setText("");
        statusText.setText("Letras falladas: ");
        contador=0;
        palabra = objeto.obtenerPalabra();
        palabraOculta = objeto.obtenerPalabraOculta(palabra);
        textoPalabra.setText(palabraOculta);
        tiempoInicial = System.currentTimeMillis();
    }
    
